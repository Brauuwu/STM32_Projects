int8_t TM1637_coding(uint8_t DispData){
	uint8_t PointData;
	if(PointFlag) PointData = 0x80;
	else PointData = 0;
	
	if(DispData == 0x7f) DispData = 0x00 + PointData; // 0x7f ???????? ???????? ??? ?????????? ????? ?????
	else DispData = digitToSegment[DispData] + PointData;
	return DispData;
}
